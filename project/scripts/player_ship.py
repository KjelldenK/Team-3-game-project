class Player_Ship:

    def __init__(self):
        self.sprit = ""
        self.ship_speed = 100
        self.ship_attack_speed = 10
        self.ship_x = 400
        self.ship_y = 100

    
    def get_sprit(self):
        return self.sprit

    def get_ship_speed(self):
        return self.ship_speed

    def get_ship_attack_speed(self):
        return self.ship_attack_speed

    def get_ship_x(self):
        return self.ship_x

    def get_ship_y(self):
        return self.ship_y








